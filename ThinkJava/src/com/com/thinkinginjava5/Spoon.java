/**
 * Java允许将多个静态初始化动作组织成一个特殊的
 * "静态子句"（有时也叫做"静态块"）。
 */
public class Spoon {
    static int i;
    static {
        i = 47;
    }
    //尽管上面的代码看起来像个方法，但它实际只是一段跟在static关键字后面的代码。与其他
//    静态初始化动作一样，这段代码仅执行一次：当首次生成这个类的一个对象时，或者首次访问
//    属于那个类的静态数据成员时（即便从未生成过那个类的对象）。
}
